   ID_VND_PAYTO INTEGER  NOT NULL
  ,DATE_ADD     DATE  NOT NULL
  ,ID_VOUCH     INTEGER  NOT NULL PRIMARY KEY 
  ,ID_INVC_VND  VARCHAR(15) NOT NULL
  ,REF          VARCHAR(15)
  ,REF_2        VARCHAR(6)
  ,AMT_INVC     NUMERIC(9,2) NOT NULL
);

